#Mostrar seu dobro, triplo e raiz quadrada

num_1 = int(input("Digite um numero inteiro: "))

dobro = num_1 * 2
triplo = num_1 * 3
raiz = num_1 ** (1/2)

print(f"O dobro de {num_1} é {dobro}")
print(f"O triplo de {num_1} é {triplo}")
print(f"A raiz quadrada de {num_1} é {raiz}")
